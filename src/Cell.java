public class Cell {
    private int x;
    private int y;
    private int size;
    private int row;
    private int column;

    public Cell() {
    }

    public Cell(int x, int y, int size, int row, int column) {
        this.x = x;
        this.y = y;
        this.size = size;
        this.row = row;
        this.column = column;
    }
}